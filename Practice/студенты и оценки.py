'''Даны два списка одинаковой длины: список имен студентов и список оценок, выставленных преподавателем. 
Напишите функцию student_scores(students, scores), которая создаст словарь, где ключами будут имена студентов,
а значениями — списки пар вида (оценка, номер оценки), отсортированные по номеру оценки.

Номер оценки соответствует индексу элемента в списке оценок плюс единица (так как индексы начинаются с нуля, а
номера должны начинаться с единицы).

Требования:

    Используйте функцию enumerate() для удобного доступа к индексам и элементам списка.
    Отсортируйте пары внутри каждого списка по номеру оценки.
    Убедитесь, что ключи уникальны, и учитывайте случай, когда студенты имеют одинаковые имена.

Параметры:

    students: список строковых значений (имен студентов),
    scores: список целых чисел (оценок).

Возвращаемое значение:

Словарь, где ключ — имя студента, а значение — список кортежей вида (оценка, номер_оценки), отсортированный по номеру оценки.
Ограничения:

    Длины обоих списков совпадают и лежат в пределах от 1 до 100.
    Оценки — целые числа от 0 до 100.

Пример:
students = ["Игорь", "Анна", "Сергей"]
scores = [85, 92, 78]
student_scores(students, scores)

Ожидаемый результат:
{
    'Игорь': [(85, 1)],
    'Анна': [(92, 2)],
    'Сергей': [(78, 3)]
}

Пример:
students = ["Иван", "Елена", "Иван", "Алина"]
scores = [90, 85, 95, 88]
student_scores(students, scores)
  Ожидаемый результат:
  {
    'Иван': [(90, 1), (95, 3)],  # Обратите внимание, что оценка 95 идёт третьей по порядку!
    'Елена': [(85, 2)],
    'Алина': [(88, 4)]
}

Подсказка: подумайте, как аккуратно добавить новые записи в словарь, учитывая возможное дублирование
имен студентов. Используйте функцию enumerate() для удобной обработки индекса и элемента списка одновременно.'''


def student_scores(students, scores):
    result = {}
    for index, (student, score) in enumerate(zip(students, scores), start=1):
        if student not in result:
            result[student] = []
        result[student].append((score, index))    
    return result

students = ["Иван", "Елена", "Иван", "Алина"]
scores = [90, 85, 95, 88]
print(student_scores(students, scores))

students = ["Игорь", "Анна", "Сергей"]
scores = [85, 92, 78]
print(student_scores(students, scores))
